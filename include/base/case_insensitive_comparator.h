#ifndef koobika_hook_base_caseinsensitivecomparator_h
#define koobika_hook_base_caseinsensitivecomparator_h

#include <string>
#include <algorithm>

namespace koobika::hook::base {
// =============================================================================
// CaseInsensitiveComparator                                           ( class )
// -----------------------------------------------------------------------------
// This specification holds for generic case insensitive hash generator.
// =============================================================================
struct CaseInsensitiveComparator {
  bool operator()(const std::string& l, const std::string& r) const {
    return l.size() == r.size() &&
           std::equal(l.begin(), l.end(), r.begin(),
                      [](char a, char b) { return tolower(a) == tolower(b); });
  }
};
}  // namespace koobika::hook::base

#endif