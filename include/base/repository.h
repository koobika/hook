#ifndef koobika_hook_base_repository_h
#define koobika_hook_base_repository_h

#include <optional>

namespace koobika::hook::base {
// =============================================================================
// Repository                                                      [ interface ]
// -----------------------------------------------------------------------------
// This specification holds for the repository interface.
// -----------------------------------------------------------------------------
// Template parameters:
//    KEty - key type.
//    VAty - value type.
// =============================================================================
template <typename KEty, typename VAty>
class Repository {
 public:
  // ---------------------------------------------------------------------------
  // Methods                                                          [ public ]
  // ---------------------------------------------------------------------------
  // Sets specified key/value pair.
  virtual void Set(const KEty&, const VAty&) = 0;
  // Erases specified key.
  virtual void Erase(const KEty&) = 0;
  // Clears all the information.
  virtual void Clear() = 0;
  // Gets the specified value using its key.
  virtual std::optional<VAty> Get(const KEty&) const = 0;
};
}  // namespace koobika::hook::base

#endif